let numeroTexto = "123";
let suma = numeroTexto + 10;
let resta = numeroTexto - 10;

console.log(suma);
console.log(resta);

//El operador + en JavaScript puede realizar una concatenación de cadenas, y si uno de los operandos es un string, convierte el otro operando a cadena.
//El operador - siempre realiza una operación aritmética, y si uno de los operandos es un string que puede ser convertido a número, JavaScript realiza esa conversión automáticamente.